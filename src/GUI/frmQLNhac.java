    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String selTB =(String) jTable1.getModel().getValueAt(jTable1.getSelectedRow(), 1);
        BaiHat bh = new BaiHat(Integer.parseInt(txtMaBH.getText()), txtTenBH.getText(),arLoiBH.getText(), rdBQCo.isSelected());
        LoaiNhacDTO ln = lnBUS.getDichVuByNameBUS((String) jlLoaiBH.getSelectedValue());
        if(ln!=null){
            int i=0;
            while (i<arrLN.size()) {                
                if(arrLN.get(i).getId()==ln.getId()){
                    int j =0;
                    boolean k=true;
                    while(k) {                
                        if(arrLN.get(i).getArrBaiHat().get(j).getName()==selTB){
                            arrLN.get(i).getArrBaiHat().remove(arrLN.get(i).getArrBaiHat().get(j));
                            k=false;
                        }                       
                        j++;
                    }
                    showTB(arrLN.get(i));
                }
                i++;
            }                                   
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed
    private void showTB(LoaiNhacDTO ln){
        DefaultTableModel tableMode = (DefaultTableModel) jTable1.getModel();
        tableMode.setRowCount(0);
        for(BaiHat bh:ln.getArrBaiHat()){                                 
            tableMode.addRow(new Object[]{bh.getId(),bh.getName(),bh.getLyric(),bh.isBanQuyen()});
        }
    }
    public boolean checkBaiHatByName(LoaiNhacDTO ln,int id){      
        for(BaiHat bh:ln.getArrBaiHat()){         
            if(bh.getId()==(id)){
                return true;           
            }
        }
        return false;
    }
