    public void inItData(){
        
        EntityManagerFactory entityManagerFactory = Persistence.createEntityManagerFactory("QLNhacPU");
        entityManager = entityManagerFactory.createEntityManager();
        entityManager.getTransaction().begin();
        ArrayList<Loainhac> listLoaiNhac;
        Query q= entityManager.createNamedQuery("Loainhac.findAll");
        listLoaiNhac = new ArrayList<Loainhac>( q.getResultList());
        for(Loainhac ln:listLoaiNhac){
            LoaiNhacDTO loaiNhacDTO = new LoaiNhacDTO(ln.getId(),ln.getName());
            arrLN.add(loaiNhacDTO);            
        }
        entityManager.close();      
    
    }   
    public void upDate(){
        jListModel.clear();
        for(LoaiNhacDTO ln:arrLN)
            jListModel.addElement(ln.getName());
        jlLoaiBH.setModel(jListModel);
            
    }
