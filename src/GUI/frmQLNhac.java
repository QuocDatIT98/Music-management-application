    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        BaiHat bh = new BaiHat(Integer.parseInt(txtMaBH.getText()), txtTenBH.getText(),arLoiBH.getText(), rdBQCo.isSelected());
        LoaiNhacDTO ln = lnBUS.getDichVuByNameBUS((String) jlLoaiBH.getSelectedValue());
        if(ln!=null){
            int i=0;
            while (i<arrLN.size()) {                
                if(arrLN.get(i).getId()==ln.getId()){
                    if(!checkBaiHatByName(arrLN.get(i), Integer.parseInt(txtMaBH.getText()))){
                        arrLN.get(i).getArrBaiHat().add(bh);
                        showTB(arrLN.get(i));
                    }else
                        JOptionPane.showMessageDialog(null, "Mã bị Trùng");
                }
                i++;
            }                                   
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

